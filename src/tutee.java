
public class tutee {
	private String name;
	private String email;
	public tutee(String nName, String nEmail) {
		name = nName;
		email = nEmail;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
